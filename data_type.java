public class data_type {
    public static void main(String[] args) {
        
    // In this section all dat types are Primitive
    
    int num = 5; // int store 4 bytes
    byte by = 127; // byte store 1
    short sh = 32767; // short store 2
    long l = 9223372036854775807L; // long store 8

    char c = 'D'; // character store 2 bytes because java actually
    // follows something called unicode not ASCII value by default
    // but different language just like C store the character value 1 byte

    double d = 5.5; // double store 8 bytes
    float f = 5.5f; // float store 4 bytes
    boolean b = true; // boolean store 1 byte
    }
}
